def mul(num1, num2):
    res = num1 * num2
    return res


def div(num3, num4):
    res1 = num3 / num4
    return res1


def add(num1, num2):
    res = num1 + num2
    return res


def mod(num1, num2):
    res = num1 % num2
    return res


def idk(num1, num2):
    res = num1 // num2
    return res


def sub(num1, num2):
    res = num1 - num2
    return res


